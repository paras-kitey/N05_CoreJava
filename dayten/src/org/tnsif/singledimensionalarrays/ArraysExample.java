// Program to demonstrate on arrays using compile time input values 
package org.tnsif.singledimensionalarrays;

public class ArraysExample {

	public static void main(String[] args) {
		
		// declaration and initialization at compile-time
		
		int arr[]= {12,52,58,88};
		
		// enhanced for loop for printing array
		for(int i:arr)
		{
			System.out.print(i+" ");		}
		
	}

}
